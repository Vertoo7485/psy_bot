      def handle_kindness_input(input_text)
        current_step = get_day_data('current_step')
        
        return false if input_text.blank?
        
        case current_step
        when 'intro'
          # –ü–µ—Ä–µ—Ö–æ–¥–∏–º –∫ –ø–ª–∞–Ω–∏—Ä–æ–≤–∞–Ω–∏—é
          store_day_data('current_step', 'planning')
          start_kindness_step('planning')
          return true
          
        when 'planning'
          # –°–æ—Ö—Ä–∞–Ω—è–µ–º –≤—ã–±—Ä–∞–Ω–Ω—ã–µ –∞–∫—Ç—ã –¥–æ–±—Ä–æ—Ç—ã
          acts = input_text.split(',').map(&:strip)
          
          if acts.length >= 3
            store_day_data('planned_acts', acts)
            store_day_data('current_step', 'execution')
            start_kindness_step('execution')
            return true
          else
            send_message(text: "–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –≤—ã–±–µ—Ä–∏—Ç–µ 3 –∞–∫—Ç–∞ –¥–æ–±—Ä–æ—Ç—ã (—Ä–∞–∑–¥–µ–ª—è–π—Ç–µ –∑–∞–ø—è—Ç—ã–º–∏).")
            return false
          end
          
        when 'execution'
          # –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –ø–æ–¥—Ç–≤–µ—Ä–¥–∏–ª –≤—ã–ø–æ–ª–Ω–µ–Ω–∏–µ
          if input_text.downcase.include?('–≥–æ—Ç–æ–≤–æ') || input_text.downcase.include?('done') || input_text == '‚úÖ'
            store_day_data('execution_confirmed', true)
            store_day_data('current_step', 'reflection')
            start_kindness_step('reflection')
            return true
          else
            send_message(text: "–ö–æ–≥–¥–∞ –∑–∞–∫–æ–Ω—á–∏—Ç–µ –≤—Å–µ 3 –∞–∫—Ç–∞ –¥–æ–±—Ä–æ—Ç—ã, –Ω–∞–ø–∏—à–∏—Ç–µ '‚úÖ –ì–æ—Ç–æ–≤–æ!'")
            return false
          end
          
        when 'reflection'
          # –°–æ—Ö—Ä–∞–Ω—è–µ–º —Ä–µ—Ñ–ª–µ–∫—Å–∏—é
          store_day_data('reflection', input_text)
          store_day_data('current_step', 'integration')
          start_kindness_step('integration')
          return true
          
        when 'integration'
          # –°–æ—Ö—Ä–∞–Ω—è–µ–º –≤—ã–±—Ä–∞–Ω–Ω—É—é –ø—Ä–∞–∫—Ç–∏–∫—É
          store_day_data('integration_commitment', input_text)
          
          # –í—Å–µ —à–∞–≥–∏ –≤—ã–ø–æ–ª–Ω–µ–Ω—ã
          send_message(
            text: "‚ú® *–ü–æ–∑–¥—Ä–∞–≤–ª—è–µ–º! –í—ã –ø—Ä–æ—à–ª–∏ –≤—Å–µ —à–∞–≥–∏ –ø—Ä–∞–∫—Ç–∏–∫–∏ –¥–æ–±—Ä–æ—Ç—ã!* ‚ú®\n\n–ù–∞–∂–º–∏—Ç–µ –∫–Ω–æ–ø–∫—É, —á—Ç–æ–±—ã –∑–∞–≤–µ—Ä—à–∏—Ç—å —É–ø—Ä–∞–∂–Ω–µ–Ω–∏–µ:",
            reply_markup: day_15_exercise_completed_markup
          )
          return true
        end
        
        false
      end
      
