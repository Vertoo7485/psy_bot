          'level1_action', 'level2_immediate_goal', 'level3_functional_value',
          'level4_personal_value', 'level5_relational_value', 'level6_existential_value'
        ]
        
        current_index = levels.index(level_name)
        if current_index && current_index < levels.size - 1
          next_level = levels[current_index + 1]
          start_exercise_step(next_level)
        elsif level_name == 'level6_existential_value'
          start_exercise_step('identify_gaps')
        else
          start_exercise_step('identify_gaps')
        end
        
        true
      end
      
